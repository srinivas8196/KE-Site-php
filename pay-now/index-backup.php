                    success: function(response) {
                        if (response.error) {
                            alert(response.error); // Display error message from backend
                            return;
                        }

                        // Configure Razorpay payment options
                        var options = {
                            key: "rzp_live_D5lhKkL7KTKaGs", // Replace with your Razorpay key
